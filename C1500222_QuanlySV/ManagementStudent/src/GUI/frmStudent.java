    private static Object[] toObjectData(student sv) {
        SimpleDateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy");
        String date = dateFormat.format(sv.getStdBirth());
        boolean flag = sv.isStdGender();
        String gioitinh = "Female";
        if (!flag) {
            gioitinh = "Male";
        }
        Object[] objectData = {sv.getStdID(), sv.getStdName(), sv.getClassID(),
            sv.getStdLevel(), date, sv.getStdAddress(), gioitinh, sv.getStdPhone(), sv.getStdSchoolyear()};
        return objectData;
    }


