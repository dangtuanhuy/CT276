    public boolean checkinfo() {
        check c = new check();
        if (!c.checkID(txtID.getText())) {
            JOptionPane.showMessageDialog(this, "Student ID invalid", "Error", JOptionPane.ERROR_MESSAGE);
            txtID.setText("");
            txtID.requestFocus();
            return false;
        } else if (!c.checkSpace(txtName.getText()) || !c.check(txtName.getText())) {
            JOptionPane.showMessageDialog(this, "Student Name invalid", "Error", JOptionPane.ERROR_MESSAGE);
            txtName.setText("");
            txtName.requestFocus();
            return false;

        } else if (!c.checkSpace(txtAddress.getText())) {
            JOptionPane.showMessageDialog(this, "Address invalid", "Error", JOptionPane.ERROR_MESSAGE);
            txtAddress.setText("");
            txtAddress.requestFocus();
            return false;

        } else if (!c.checkSpace(txtPhone.getText()) || !c.checkPhone(txtPhone.getText())) {
            JOptionPane.showMessageDialog(this, " Phone invalid", "Error", JOptionPane.ERROR_MESSAGE);
            txtPhone.setText("");
            txtPhone.requestFocus();
            return false;
        }
        return true;
    }

