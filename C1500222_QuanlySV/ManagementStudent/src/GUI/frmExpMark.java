    public String loadXepLoai(float diem4) {
        String xeploai = "";
        if (diem4 < 1) {
            xeploai = "Failed";
        } else if (diem4 >= 1 && diem4 < 2) {
            xeploai = "Below arg.";
        } else if (diem4 >= 2 && diem4 < 2.5) {
            xeploai = "Average";
        } else if (diem4 >= 2.5 && diem4 < 3.2) {
            xeploai = "Average +";
        } else if (diem4 >= 3.2 && diem4 < 3.6) {
            xeploai = "Good";
        } else if (diem4 > 3.6) {
            xeploai = "Excellent";
        }
        return xeploai;
    }

