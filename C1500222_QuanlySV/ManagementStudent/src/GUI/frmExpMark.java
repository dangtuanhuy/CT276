    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        String sv = listStudent.get(cbxClassId.getSelectedIndex()).getStdID();
        String hocky = (String) cbxHocky.getSelectedItem();
        ArrayList<mark> stds = new markMain().findByIDStudent(sv, hocky);
        for (mark std : stds) {
            Vector v = new Vector();
            v.add(std.getMamh());
            v.add(std.getDiem());
            v.add(std.getDiemchu());
            dtm.addRow(v);
        }
        try {
            int count = dtm.getRowCount();
            if (count > 0) {
                float sum = 0;
                float sum4 = 0;
                for (int i = 0; i < count; i++) {
                    float diem = 0;

                    if ("A".equals(tableMark.getValueAt(i, 2))) {
                        diem = 4.0f;
                        sum4 = sum4 + diem;
                    } else if ("B+".equals(tableMark.getValueAt(i, 2))) {
                        diem = 3.5f;
                        sum4 = sum4 + diem;
                    } else if ("B".equals(tableMark.getValueAt(i, 2))) {
                        diem = 3.0f;
                        sum4 = sum4 + diem;
                    } else if ("C+".equals(tableMark.getValueAt(i, 2))) {
                        diem = 2.5f;
                        sum4 = sum4 + diem;
                    } else if ("C".equals(tableMark.getValueAt(i, 2))) {
                        diem = 2.0f;
                        sum4 = sum4 + diem;
                    } else if ("D+".equals(tableMark.getValueAt(i, 2))) {
                        diem = 1.5f;
                        sum4 = sum4 + diem;
                    } else if ("D".equals(tableMark.getValueAt(i, 2))) {
                        diem = 1.0f;
                        sum4 = sum4 + diem;
                    } else if ("F".equals(tableMark.getValueAt(i, 2))) {
                        diem = 0.0f;
                        sum4 = sum4 + diem;
                    }
                    sum = sum + Float.parseFloat(tableMark.getValueAt(i, 1).toString());
                }
                float dtb4 = (float) sum4 / count;
                float dtb = (float) sum / count;

                String mark10 = dtb + " ";
                String mark4 = dtb4 + " ";

                String xl = loadXepLoai(dtb4);

                txtKind.setText(xl);
                txtMark.setText(mark10);
                txtMark2.setText(mark4);

                // Student student = new Student(masv, diem10, diem4, xeploai);
                //   Student update = new StudentDao().upDateXepLoai(student);
            } else {
                txtMark.setText("0");
                txtMark2.setText("0");
                txtKind.setText("Have not mark, have not grade!");
            }
        } catch (NumberFormatException | ArrayIndexOutOfBoundsException e) {
            e.printStackTrace();
        }
    }
